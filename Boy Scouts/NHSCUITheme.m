#import "NHSCUITheme.h"

@implementation NHSCUITheme


+ (UIColor *)getCustomizedUIColorGreen
{
    return [UIColor colorWithRed:0.33 green:0.85 blue:0.11 alpha:1];
}

+ (UIColor *)getCustomizedUIColorRed
{
    return [UIColor colorWithRed:1.00 green:0.20 blue:0.22 alpha:1];
}

+ (UIColor *)getCustomizedUIColorPurple
{
    return [UIColor colorWithRed:0.72f green:0.44f blue:0.75f alpha:1.0f];
}

@end
